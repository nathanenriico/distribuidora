// Adiciona event listener após DOM carregar
document.addEventListener('DOMContentLoaded', function() {
    // Adiciona botões mobile após carregar novos produtos
    const observer = new MutationObserver(function(mutations) {
        mutations.forEach(function(mutation) {
            if (mutation.type === 'childList') {
                const newCards = document.querySelectorAll('.product-card:not(.mobile-btn-added)');
                newCards.forEach(card => {
                    const button = document.createElement('button');
                    button.className = 'mobile-add-btn';
                    button.innerHTML = '+';
                    button.onclick = function(e) {
                        e.stopPropagation();
                        addToCart(card);
                    };
                    card.appendChild(button);
                    card.classList.add('mobile-btn-added');
                });
            }
        });
    });
    
    const productsGrid = document.querySelector('.products-grid');
    if (productsGrid) {
        observer.observe(productsGrid, { childList: true, subtree: true });
    }
});